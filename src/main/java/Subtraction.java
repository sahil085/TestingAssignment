
public class Subtraction {
    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 26;
    }

    int subtract(int number1, int number2) {
        return number1 - number2;
    }

    float subtract(float num1, float num2) {
        return num1 - num2;
    }

    double subtract(double num1, double num2) {
        return num1 - num2;
    }

}
